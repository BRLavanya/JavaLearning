package OOPSConcepts2;

public class A {
	
	A()
	{
		System.out.println("A class constructor with no parameter");
	}
	
	public A(int i)
	{
		System.out.println("A class constructor with 1 parameter "+i);
	}
	
	 A(int i,int j)
	{
		System.out.println("A class constructor with 2 parameter "+i +" "+j);
	}
	
	
}
